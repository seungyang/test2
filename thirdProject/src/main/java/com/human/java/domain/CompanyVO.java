package com.human.java.domain;

public class CompanyVO {

	private String company_id;
	private String company_password;
	private String company_name;
	private String company_ph;
	private String company_email;
	private String company_regnum;
	private String company_flag;
	
	
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public String getCompany_password() {
		return company_password;
	}
	public void setCompany_password(String company_password) {
		this.company_password = company_password;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_ph() {
		return company_ph;
	}
	public void setCompany_ph(String company_ph) {
		this.company_ph = company_ph;
	}
	public String getCompany_email() {
		return company_email;
	}
	public void setCompany_email(String company_email) {
		this.company_email = company_email;
	}
	public String getCompany_regnum() {
		return company_regnum;
	}
	public void setCompany_regnum(String company_regnum) {
		this.company_regnum = company_regnum;
	}
	public String getCustomer_flag() {
		return company_flag;
	}
	public void setCustomer_flag(String customer_flag) {
		this.company_flag = customer_flag;
	}
	
	
}
